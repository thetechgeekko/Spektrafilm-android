XYZ xyz_from_spectrum(const float* radiance) {
    // Rectangular-rule integral of radiance * CMF over the working shape.
    double X = 0.0, Y = 0.0, Z = 0.0;
    for (int i = 0; i < kSpectralSamples; ++i) {
        double r = static_cast<double>(radiance[i]);
        X += r * kCieCmf1931[i][0];
        Y += r * kCieCmf1931[i][1];
        Z += r * kCieCmf1931[i][2];
    }
    const double dlambda = static_cast<double>(kSpectralStepNm);
    XYZ out;
    out.X = static_cast<float>(X * dlambda);
    out.Y = static_cast<float>(Y * dlambda);
    out.Z = static_cast<float>(Z * dlambda);
    return out;
}

