void color_enlarger(const double* light_source, const double cc_values[3],
                    double* filtered_out) {
    double trans[3];
    for (int k = 0; k < 3; ++k)
        trans[k] = std::pow(10.0, -(cc_values[k] / 100.0));
    for (int l = 0; l < 81; ++l) {
        double total = 1.0;
        for (int k = 0; k < 3; ++k) {
            const double dimmed =
                1.0 - (1.0 - kCustomDichroicFilters[l][k]) * (1.0 - trans[k]);
            total *= dimmed;
        }
        filtered_out[l] = light_source[l] * total;
    }
}

